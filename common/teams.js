function getTeamById(id) {
  const team = Object.create(teams[id]); //does a deep copy
  if(!team)
    throw "teamId is not found!";
  const mainColor = team["mainColor"];
  const secondaryColor = team["secondaryColor"];
  let teamObj = {};
  teamObj["mainColor"] = team["colors"][mainColor]["hex"];
  teamObj["secondaryColor"] = team["colors"][secondaryColor]["hex"];
  teamObj["abreviation"] = team["abreviation"];
  teamObj["imgUrl"] = team["imgUrl"];
  teamObj["fullName"] = team["fullName"];
  return teamObj;
}

function getMainColor(id) {
  const team = getTeamById(id);
  return team["mainColor"];
}

function getSecondaryColor(id) {
  const team = getTeamById(id);
  return team["secondaryColor"];
}

function getImage(id) {
  const team = getTeamById(id);
  return team['imgUrl'];
}

